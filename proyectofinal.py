# RK4

def acceleration(x, y, vx, vy, k=bend_strength):
    """Aceleración radial tipo -k * r_hat / r^2 (visual, no exacta relativista).
       k controla la intensidad de la desviación."""
    r = np.sqrt(x*x + y*y)
    if r == 0:
        return 0.0, 0.0
    ax = -k * x / (r**3)
    ay = -k * y / (r**3)
    return ax, ay

def rk4_step(x, y, vx, vy, dt):
    """Un paso RK4 para sistema x' = vx, vx' = ax(x,y,vx,vy)."""
    ax1, ay1 = acceleration(x, y, vx, vy)
    k1x, k1y = vx, vy
    k1vx, k1vy = ax1, ay1

    ax2, ay2 = acceleration(x + 0.5*dt*k1x, y + 0.5*dt*k1y,
                           vx + 0.5*dt*k1vx, vy + 0.5*dt*k1vy)
    k2x, k2y = vx + 0.5*dt*k1vx, vy + 0.5*dt*k1vy
    k2vx, k2vy = ax2, ay2

    ax3, ay3 = acceleration(x + 0.5*dt*k2x, y + 0.5*dt*k2y,
                           vx + 0.5*dt*k2vx, vy + 0.5*dt*k2vy)
    k3x, k3y = vx + 0.5*dt*k2vx, vy + 0.5*dt*k2vy
    k3vx, k3vy = ax3, ay3

    ax4, ay4 = acceleration(x + dt*k3x, y + dt*k3y,
                           vx + dt*k3vx, vy + dt*k3vy)
    k4x, k4y = vx + dt*k3vx, vy + dt*k3vy
    k4vx, k4vy = ax4, ay4

    x_new = x + dt*(k1x + 2*k2x + 2*k3x + k4x)/6
    y_new = y + dt*(k1y + 2*k2y + 2*k3y + k4y)/6
    vx_new = vx + dt*(k1vx + 2*k2vx + 2*k3vx + k4vx)/6
    vy_new = vy + dt*(k1vy + 2*k2vy + 2*k3vy + k4vy)/6

    return x_new, y_new, vx_new, vy_new


def init_right_to_left(n, x_start= view_radius, y_min=-0.9, y_max=0.9):
    xs = np.full(n, x_start, dtype=float)
    ys = np.linspace(y_min, y_max, n, dtype=float)
    vxs = -0.6 * np.ones(n)   # velocidad inicial hacia la izquierda
    vys = np.zeros(n)
    return xs, ys, vxs, vys

def init_circle_in(n, radius=view_radius, center=(0.0,0.0)):
    thetas = np.linspace(0, 2*np.pi, n, endpoint=False)
    cx, cy = center
    xs = cx + radius * np.cos(thetas)
    ys = cy + radius * np.sin(thetas)
    vxs = -np.cos(thetas) * 0.6
    vys = -np.sin(thetas) * 0.6
    return xs, ys, vxs, vys

def init_point_emission(n, point=(-view_radius, 0.0), spread=0.6):
    px, py = point
    angles = np.linspace(-spread, spread, n)
    xs = np.full(n, px, dtype=float)
    ys = np.full(n, py, dtype=float)
    vxs = np.cos(angles) * 0.6
    vys = np.sin(angles) * 0.6
    return xs, ys, vxs, vys

