def accel(x, y, z):
    """Aceleración gravitacional tipo Newtoniano en 3D."""
    r = np.sqrt(x*x + y*y + z*z)
    r = max(r, 1e-6)
    a = -G * M / r**3
    return a*x, a*y, a*z

def rk4(x, y, z, vx, vy, vz, dt):
    # k1
    ax1, ay1, az1 = accel(x, y, z)
    k1x, k1y, k1z = vx, vy, vz
    k1vx, k1vy, k1vz = ax1, ay1, az1

    # k2
    ax2, ay2, az2 = accel(x + 0.5*dt*k1x,
                           y + 0.5*dt*k1y,
                           z + 0.5*dt*k1z)
    k2x  = vx + 0.5*dt*k1vx
    k2y  = vy + 0.5*dt*k1vy
    k2z  = vz + 0.5*dt*k1vz
    k2vx, k2vy, k2vz = ax2, ay2, az2

    # k3
    ax3, ay3, az3 = accel(x + 0.5*dt*k2x,
                           y + 0.5*dt*k2y,
                           z + 0.5*dt*k2z)
    k3x  = vx + 0.5*dt*k2vx
    k3y  = vy + 0.5*dt*k2vy
    k3z  = vz + 0.5*dt*k2vz
    k3vx, k3vy, k3vz = ax3, ay3, az3

    # k4
    ax4, ay4, az4 = accel(x + dt*k3x,
                           y + dt*k3y,
                           z + dt*k3z)
    k4x  = vx + dt*k3vx
    k4y  = vy + dt*k3vy
    k4z  = vz + dt*k3vz
    k4vx, k4vy, k4vz = ax4, ay4, az4

    # Fusión
    xn  = x  + (dt/6)*(k1x + 2*k2x + 2*k3x + k4x)
    yn  = y  + (dt/6)*(k1y + 2*k2y + 2*k3y + k4y)
    zn  = z  + (dt/6)*(k1z + 2*k2z + 2*k3z + k4z)

    vxn = vx + (dt/6)*(k1vx + 2*k2vx + 2*k3vx + k4vx)
    vyn = vy + (dt/6)*(k1vy + 2*k2vy + 2*k3vy + k4vy)
    vzn = vz + (dt/6)*(k1vz + 2*k2vz + 2*k3vz + k4vz)

    return xn, yn, zn, vxn, vyn, vzn

